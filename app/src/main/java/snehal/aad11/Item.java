package snehal.aad11;

public class Item {
    String animalName;
    int animalImage;

    public Item(String animalName, int animalImage) {
        this.animalName = animalName;
        this.animalImage = animalImage;
    }

    public String getAnimalName() {
        return animalName;
    }


    public int getAnimalImage() {
        return animalImage;
    }

}
